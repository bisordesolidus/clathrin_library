r"""
schein.py — правило запрета «голова-хвост» Шайна: классификатор клеток.

Schein S., «Architecture of Clathrin Fullerene Cages Reflects a Geometric
Constraint—the Head-to-Tail Exclusion Rule», J. Mol. Biol. 387, 363 (2009).

Клетка РАЗРЕШЕНА ⟺ ни у одной грани нет пары DAD в аранжировке «голова-хвост».

Механизм: двугранный угол в вершине зависит от её типа (набор размеров 3 граней):
        666 → 180°,  566 → 138.2°,  556 → 119.8°,  555 → 105.2°
(углы убывают с числом пятиугольников в вершине — больше кривизны). DAD на ребре
= разность углов на его концах, вектор от меньшего к большему. «Голова-хвост» =
монотонный ход углов вдоль грани через вершину (голова одного DAD встречает
хвост следующего) — вынуждает несжимаемое кручение ноги ⇒ клетка исключена.

Работает на ДУАЛе (триангуляции): грань фуллерена = вершина дуала; её граничные
вершины = треугольники дуала вокруг неё (по вращательной системе планарного
вложения) — комбинаторно, без геометрии.

ВАЛИДАЦИЯ (тесты): счёт разрешённых изомеров совпадает с литературой /
таблицей Шайна: C28:1, C30:0, C32:1, C34:0, C36:2, C38:1, C40:2, C42:1, C44:2.
"""

from __future__ import annotations

import networkx as nx

# Двугранный угол (град) в вершине по её типу — набор размеров 3 сходящихся граней.
DIHEDRAL: dict[tuple[int, int, int], float] = {
    (5, 5, 5): 105.2,
    (5, 5, 6): 119.8,
    (5, 6, 6): 138.2,
    (6, 6, 6): 180.0,
}


def head_to_tail_counts(dual: nx.Graph) -> list[int]:
    r"""Для каждой грани фуллерена (= вершины дуала) — число «голова-хвост» шагов
    (монотонных троек двугранных углов вдоль границы грани)."""
    _, emb = nx.check_planarity(dual)
    deg = dict(dual.degree())
    counts = []
    for f in dual.nodes():
        nb = list(emb.neighbors_cw_order(f))
        k = len(nb)
        # двугранные углы вершин фуллерена вокруг грани f: треугольники (f,nb_i,nb_{i+1})
        seq = [DIHEDRAL[tuple(sorted((deg[f], deg[nb[i]], deg[nb[(i + 1) % k]])))]
               for i in range(k)]
        ht = 0
        for i in range(k):
            a, b, c = seq[(i - 1) % k], seq[i], seq[(i + 1) % k]
            if (a < b < c) or (a > b > c):        # монотонная тройка = голова-хвост
                ht += 1
        counts.append(ht)
    return counts


def is_schein_allowed(dual: nx.Graph) -> bool:
    r"""Разрешена ли клетка правилом запрета «голова-хвост» (строгая версия).
    Разрешена ⟺ ни у одной грани нет «голова-хвост» пары DAD."""
    return max(head_to_tail_counts(dual), default=0) == 0
